package com.gdi.model.dashboard;

public class RanksInfo {

    CityInfo city;
    CountryInfo country;
    GlobalInfo global;

    public CityInfo getCity() {
        return city;
    }

    public void setCity(CityInfo city) {
        this.city = city;
    }

    public CountryInfo getCountry() {
        return country;
    }

    public void setCountry(CountryInfo country) {
        this.country = country;
    }

    public GlobalInfo getGlobal() {
        return global;
    }

    public void setGlobal(GlobalInfo global) {
        this.global = global;
    }
}
